int main(){
    int num1, num2, num3;
    printf("Introduzca un numero: ");
    scanf("%d", &num1);
    printf("Introduzca otro numero: ");
    scanf("%d", &num2);
    printf("Introduzca otro numero: ");
    scanf("%d", &num3);
    printf("Suma = %d\n", num1 + num2 + num3);
    printf("Resta = %d\n", num1 - num2 - num3);
    printf("Multiplicacion = %d\n", num1 * num2 * num3);
    printf("Division = %d\n", num1 / num2 / num3);
    printf("Residuo  = %d\n", num1 % num2);
}
