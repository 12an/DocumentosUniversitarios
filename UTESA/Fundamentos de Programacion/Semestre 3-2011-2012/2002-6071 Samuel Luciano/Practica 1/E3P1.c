int main(){
    int num1, num2;
    printf("Introduzca un numero: ");
    scanf("%d", &num1);
    printf("Introduzca otro numero: ");
    scanf("%d", &num2);
    printf("Operador >: %d\n", num1 > num2);
    printf("Operador <: %d\n", num1 < num2);
    printf("Operador >=: %d\n", num1 >= num2);
    printf("Operador <=: %d\n", num1 <= num2);
    printf("Operador ==: %d\n", num1 == num2);
    printf("Operador !=: %d\n", num1 != num2);
}
