int main () {
    printf("Operadores Relacionales\n\n");
 int x;
 int y;
 printf("Introducir un valor:") ; 
 scanf("%d", &x); 
 printf("Introducir otro valor:");     
scanf("%d", &y);
 printf("Resultado: %d\n", &x<&y);
 printf("Resultado: %d\n", &x>&y);
 printf("Resultado: %d\n", &x>=&y);
 printf("Resultado: %d\n", &x<=&y);
 printf("Resultado: %d\n", &x==&y);
 printf("Resultado: %d\n", &x!=&y);


system("pause");

}


