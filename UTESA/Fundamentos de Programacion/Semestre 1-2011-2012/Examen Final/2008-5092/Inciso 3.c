int main() {
    int a, b;
    printf("Introducir un valor");
    scanf("%d", &a);
    printf("Introducir otro valor");
    scanf("%d", &b);
    printf("Resultado: %d\n", a > b);
    printf("Resultado: %d\n", a < b);
    printf("Resultado: %d\n", a >= b);
    printf("Resultado: %d\n", a <= b);
    printf("Resultado: %d\n", a != b);
    printf("Resultado: %d\n", a == b);
 system("PAUSE");   
}
